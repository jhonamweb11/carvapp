public class HorariosReservados {
    private int reservaid;
    private String dia;
    private String hora;

    public HorariosReservados(int reservaid, String dia, String hora) {
        this.reservaid = reservaid;
        this.dia = dia;
        this.hora = hora;
    }

    public int getReservaid() {
        return reservaid;
    }

    public void setReservaid(int reservaid) {
        this.reservaid = reservaid;
    }

    public String getDia() {
        return dia;
    }

    public void setDia(String dia) {
        this.dia = dia;
    }

    public String getHora() {
        return hora;
    }

    public void setHora(String hora) {
        this.hora = hora;
    }
   

 

   
    
    
    
    
    
}
